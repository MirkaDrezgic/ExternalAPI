package com.tiac.test.entities.dto;

public class CoordinateDTO {
	
	private String type;
	private Double x;
	private Double y;
	
	public CoordinateDTO() {
		super();
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public Double getX() {
		return x;
	}
	public void setX(Double x) {
		this.x = x;
	}
	public Double getY() {
		return y;
	}
	public void setY(Double y) {
		this.y = y;
	}
   
}
